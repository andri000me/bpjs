class ritp extends CI_Controller {

    function __construct() {
        parent::__construct();
        $this->load->model('ritp_model');
        $this->load->library('form_validation');
        chek_session();
    }

    public function index() {
        $ritp = $this->ritp_model->get_all();

        $data = array(
            'ritp_data' => $ritp
        );

        $this->template->display('ritp/tb_ritp_list', $data);
    }





  
    public function create() {
        $data = array(
            'button' => 'Create',
            'action' => site_url('ritp/create_action'),
            'kode_program' => set_value('kode_program'),
            'nama_program' => set_value('nama_program'),
            'uraian' => set_value('uraian'),
            'kode_akun' => set_value('kode_akun'),
            'pel' => set_value('pel'),
            'id_pelayanan'=> set_value('id_pelayanan'),
           
        );
        $this->template->display('ritp/tb_ritp_form', $data);
    }

    public function create_action() {

            $data = array(
                'kode_program' => $this->input->post('kode_program', TRUE),
                'nama_program' => $this->input->post('nama_program', TRUE),
                'uraian' => $this->input->post('uraian', TRUE),
                'kode_akun' => $this->input->post('kode_akun', TRUE),
                'pel' => $this->input->post('pel', TRUE),
                'id_pelayanan' => $this->input->post('id_pelayanan', TRUE),
                
            );

            $this->ritp_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('ritp'));
       
    }

    public function update($kd) {
        $row = $this->ritp_model->get_by_kd($kd);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('ritp/update_action'),
                'kode_program' => set_value('kode_program', $row->kode_program),
                'nama_program' => set_value('nama_program', $row->nama_program),
                'uraian' => set_value('uraian', $row->uraian),
                'kode_akun' => set_value('kode_akun', $row->kode_akun),
                'id_pelayanan' => set_value('id_pelayanan', $row->id_pelayanan),
            );
            $this->template->display('ritp/tb_ritp_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('ritp'));
        }
    }

    public function update_action() {

        
            $data = array(
                'id' => $this->input->post('id', TRUE),
                'kode_program' => $this->input->post('kode_program', TRUE),
                'nama_program' => $this->input->post('nama_program', TRUE),
                'uraian' => $this->input->post('uraian', TRUE),
                'kode_akun' => $this->input->post('kode_akun', TRUE),
                'id_pelayanan' => $this->input->post('id_pelayanan', TRUE),
            );

            $this->ritp_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('ritp'));
        
    }

    public function delete($kd) {
        $row = $this->ritp_model->get_by_kd($kd);

        if ($row) {
            $this->ritp_model->delete($kd);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('ritp'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('ritp'));
        }
    }

    public function _rules() {
        $this->form_validation->set_rules('kode_program', 'kode_program', 'trim|required');
        $this->form_validation->set_rules('nama_program', 'nama_program', 'trim|required');
        $this->form_validation->set_rules('uraian', 'uraian', 'trim|required');
        $this->form_validation->set_rules('kode_akun', 'kode_akun', 'trim|required');
        $this->form_validation->set_rules('pel', 'pel', 'trim|required');
        $this->form_validation->set_rules('id_pelayanan', 'id_pelayanan', 'trim');
        $this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

    
    

}

