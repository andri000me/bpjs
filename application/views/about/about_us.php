        	&emsp;&emsp;&emsp;BPJS Kesehatan (Badan Penyelenggara Jaminan Sosial Kesehatan) merupakan Badan Hukum Publik yang bertanggung jawab langsung kepada Presiden dan memiliki tugas untuk menyelenggarakan jaminan Kesehatan Nasional bagi seluruh rakyat Indonesia, terutama untuk Pegawai Negeri Sipil, Penerima Pensiun PNS dan TNI/POLRI, Veteran, Perintis Kemerdekaan beserta keluarganya dan Badan Usaha lainnya ataupun rakyat biasa.</br></br>

			&emsp;&emsp;&emsp;BPJS Kesehatan bersama BPJS Ketenagakerjaan (dahulu bernama Jamsostek) merupakan program pemerintah dalam kesatuan Jaminan Kesehatan Nasional (JKN) yang diresmikan pada tanggal 31 Desember 2013. Untuk BPJS Kesehatan mulai beroperasi sejak tanggal 1 Januari 2014, sedangkan BPJS Ketenagakerjaan mulai beroperasi sejak 1 Juli 2014.</br></br>

			&emsp;&emsp;&emsp;BPJS Kesehatan sebelumnya bernama Askes (Asuransi Kesehatan), yang dikelola oleh PT Askes Indonesia (Persero), namun sesuai UU No. 24 Tahun 2011 tentang BPJS, PT. Askes Indonesia berubah menjadi BPJS Kesehatan sejak tanggal 1 Januari 2014.
